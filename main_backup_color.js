    // M√©todo 1: Buscar todos los elementos que tengan fill con gradiente del cuerpo
    const elementosConGradiente = svg.querySelectorAll('[fill*="gradienteCuerpo"]');
    console.log('üéØ Elementos con gradiente del cuerpo encontrados:', elementosConGradiente.length);
    
    elementosConGradiente.forEach((elemento, index) => {
        if (color && color !== '#FAFAFA') {
            elemento.setAttribute('fill', color);
            console.log(`‚úÖ Color ${color} aplicado al elemento ${index + 1}`);
        }
    });
    
    // M√©todo 2: Buscar rect√°ngulo principal por posici√≥n y tama√±o
    const rectPrincipal = svg.querySelector('rect[x="80"][y="120"][width="240"][height="140"]');
